# This is just a string, not a tuple
tup = 'Hello'

# len() counts the number of characters in the string, so this prints 5
print(len(tup))

# This is a one-item tuple because of the comma at the end
tup = 'Hello',

# len() counts the number of items in the tuple, so this prints 1
print(len(tup))